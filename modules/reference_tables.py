import numpy as np

# ─────────────────────────────────────────────────────────────────────────
# Таблица B.4 — HCl: коэффициент диффузии (м²/с) и молярная концентрация (кг/л)
# ─────────────────────────────────────────────────────────────────────────
TABLE_B4_HCl = {
    5:  2.44e-9, 10: 2.50e-9, 15: 2.55e-9,
    20: 2.60e-9, 25: 2.65e-9, 30: 2.70e-9,
}
TABLE_B4_C_ms = {
    5: 0.052, 10: 0.108, 15: 0.167,
    20: 0.230, 25: 0.297, 30: 0.367,
}


def get_D_os(C0_percent: float) -> float:
    """Коэффициент диффузии HCl при 0 °C, м²/с — линейная интерполяция."""
    keys = sorted(TABLE_B4_HCl.keys())
    return float(np.interp(C0_percent, keys, [TABLE_B4_HCl[k] for k in keys]))


def get_C_ms(C0_percent: float) -> float:
    """Молярная концентрация HCl, кг/л."""
    keys = sorted(TABLE_B4_C_ms.keys())
    return float(np.interp(C0_percent, keys, [TABLE_B4_C_ms[k] for k in keys]))


# ─────────────────────────────────────────────────────────────────────────
# Таблица B.20 — коэффициент диффузии HF (м²/с) по концентрации HF, %
# (значения D_г·1e-9; дублируются в gko_config.json для редактирования в UI)
# ─────────────────────────────────────────────────────────────────────────
TABLE_B20_HF = {
    1: 2.30e-9, 2: 2.42e-9, 3: 2.55e-9, 4: 2.66e-9, 5: 2.74e-9,
}


def get_D_HF(C_HF_percent: float, table: dict | None = None) -> float:
    """Коэффициент диффузии HF, м²/с — линейная интерполяция табл. В.20.

    table — опциональный dict {C_HF: D_g(м²/с)} из gko_config (приоритетно)."""
    src = table if table else TABLE_B20_HF
    keys = sorted(src.keys())
    return float(np.interp(C_HF_percent, keys, [src[k] for k in keys]))


